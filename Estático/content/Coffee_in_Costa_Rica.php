            Four wooden oxcarts have gathered in El Guarco de Cartago with the mission of recreating an event that first took place in 1832. This was the date when after crossing hills, mountains, rivers, deep ravines and countless natural obstacles, these carts
            and their drivers delivered the first consignment of Costa Rica coffee to the outside world.<br/><br/>
            It is clear, luminous February afternoon. The owner of the oxen and painted oxcarts-which have proclaimed part of the Cultural Heritage
            of Humanity by UNESCO-- feel very proud to revive a tradition and experience first-hand the vicissitudes faced by the pioneers who transported the first sacks of coffee from the Central Valley to Puntarenas, a spit of sand on the Pacific coast.
            From there small boats ferried the coffee to a ship that sailed to the port of Valparaiso, Chile. There, English merchants relabeled it &quot;Valpara√≠so Coffee&quot; and sent it to the United Kingdom, in a long and difficult voyage through
            the Straits of Magellan and around Tierra del Fuego to the North Atlantic.<br/><br/>
            The rumbling of the oxcarts, the puffing and painting of the oxen adorned with all their finery, the maneuvering of the cargo under the strong rays of the sun,
            and the shouts of the ox-herders- - including two young girls who learned the trade from their father-offer only a slight glimpse of the odyssey experienced by the original caravans. Then, the oxcarts traversed the narrow and slippery mule
            tracks, which were the only means of transporting cargo to the coast, on journeys that lasted four, five or six days.<br/><br/> 
            All indications are the Costa Rica was the first country in Central America and among the first in the Americas to cultivate
            coffee and sell it to Europe. At that time, only a handful of Caribbean islands governed by the English and the French were involved in the coffee production on this side of the globe. In those years the Denomination of Origin was paid scant
            attention, and coffee aficionados in the Old World drank blends of Java, Ceylon and the most ancient production sites in Asia and the Middle East. The provenance of coffee was largely unknown to consumers, which meant that the coffees of those
            regions were not the cult phenomenon they are today.
