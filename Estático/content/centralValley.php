        <p><span style="font-weight: bold;">Description:</span> The Central Valley is made by the provinces San José, Heredia and Alajuela. It is the most populated region, where the capital San José is found and in which the coffee plantations began and
            latter were carried to the other producing regions in the country. In the Central Valley are familiar figures volcanoes such as Irazu, Barva and Poas, which nourish the soils where coffee is cultivated.</p>
        <p><span style="font-weight: bold;">Characteristics:</span> This region has well-defined the wet and dry seasons, with a total rainfall of 3,000 mm in 155 days a year and a humidity of 84%. The temperature of 20ºC is in stable and sunshine average
            from 44 to 54% and 2,150 hours.</p>
        <p><span style="font-weight: bold;">Height and soils:</span> The region extends from 800 to 1,600 meters above sea level, however more than 80% of the coffee trees are grown between 1,000 and 1,400 meters. The sub-regions are distributed among lower
            areas (below 1,000 meters) where coffee is lighter, and higher area (above 1,200 meters) with stronger or more acidic coffee and also more aromatic. The altitude of the Central Valley affects the size and hardness of the coffee bean and influences
            certain components of the quality of the drink, especially its acidity; these elements added to the characteristics of Arabica coffee, provide an aromatic, delicate and tasty drink. Soils in this region have a slight degree of tropical acidity
            product of the volcanic ashes; they are rich in organic matter which favors moisture retention, a good distribution of the roots, and facilitates oxygenation. This combination favor vigor to the coffee plant, which contributes to its excellent
            quality.
            <p><span style="font-weight: bold;">Harvest season:</span> The cropping runs from November to mid-March.</p>
